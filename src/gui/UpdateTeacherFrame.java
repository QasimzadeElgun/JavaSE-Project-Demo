    private void saveIdActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveIdActionPerformed
        try {
            teacher.setId(Integer.valueOf(Idtxt.getText()));
            teacher.setName(nametxt.getText());
            teacher.setSurname(surnametxt.getText());
            teacher.setActive(Integer.valueOf(activetxt.getText()));
            boolean isUpdate = td.updateTeacher(teacher);
            if (isUpdate) {
                JOptionPane.showMessageDialog(null, "Update Teacher", "Teacher has been updated", HEIGHT);
                this.dispose();
            } else {
                JOptionPane.showMessageDialog(null, "Update Teacher", "Error!", HEIGHT);
            }

        } catch (Exception ex) {
            ex.printStackTrace();
        }

    }//GEN-LAST:event_saveIdActionPerformed

