    private void showTeacherInfo() {
        try {
            teacher = td.getTeacherById(selectedId);
            Idtxt.setText(String.valueOf(teacher.getId()));
            nametxt.setText(teacher.getName());
            surnametxt.setText(teacher.getSurname());
            activetxt.setText(String.valueOf(teacher.getActive()));

        } catch (Exception ex) {
            ex.printStackTrace();
        }
    }
